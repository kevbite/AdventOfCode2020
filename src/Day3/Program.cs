public static class Day3Solver
{
    public enum Piece
    {
        Space,
        Tree
    }
    public static long Part1(string input)
    {
        return GetTreeCollisionCount(input, (3, 1));
    }

    private static long GetTreeCollisionCount(string input, (int x, int y) moveBy)
    {
        var list = input.Split(Environment.NewLine)
            .Select(x => Stuff(x))
            .ToList();

        var treeCount = 0;
        
        for (var pos = (x: 0,y: 0); pos.y < list.Count; pos = (pos.x + moveBy.x, pos.y + moveBy.y))
        {
            if (list[pos.y].ElementAt(pos.x) == Piece.Tree)
                treeCount++;
        }
        
        return treeCount;
    }
    public static long Part2(string input)
    {
        return new []
        {
            GetTreeCollisionCount(input, (1, 1)),
            GetTreeCollisionCount(input, (3, 1)),
            GetTreeCollisionCount(input, (5, 1)),
            GetTreeCollisionCount(input, (7, 1)),
            GetTreeCollisionCount(input, (1, 2))
        }.Aggregate((a, b) => a * b);
    }
    
    public static IEnumerable<Piece> Stuff(string s)
    {
        while (true)
        {
            foreach (var c in s)
            {
                yield return c switch
                {
                    '.' => Piece.Space,
                    '#' => Piece.Tree,
                    _ => throw new ArgumentException()
                };
            }
        }
    }
}